"""
Write a program in a file names triangle.py to compute the area of a triangle. Issue the appropriate prompts for the triangle's
base and height. Then, use the formula .5 * base * height to compute the area. Test the program for an IDLE window.
"""

base = float(input("Enter the base of the triangle: "))
height = float(input("Enter the height of the triangle: "))
area = 0.5 * base * height
print("Area:", area)
